// var (Manera inicial de declarla, ahora NO se usa salvo en casos excepcionales. Puede acceder a la variable antes de declararla, no nos interesa eso)
 
var helloWorld = "¡Hello, JavaScript!"
console.log(helloWorld)

helloWorld = "¡Hola de nuevo, JavaScript!"
console.log(helloWorld)

// let (No puede acceder a la variable antes de declararla)

let helloWorld2 = "¡Hello, JavaScript 2!"
console.log(helloWorld2)

helloWorld2 = "¡Hola de nuevo, JavaScript 2!"
console.log(helloWorld2)

// const (No se puede reasignar un valor a la constante)

const helloWorld3 = "¡Hello, JavaScript 3!"
console.log(helloWorld3)

